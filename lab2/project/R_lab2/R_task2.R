# Задание 2 – Написать программу вычисляющую площадь неправильного многоугольника. Многоугольник на плоскости
# задается целочисленными координатами своих N вершин в декартовой системе. Стороны многоугольника
#  не соприкасаются (за исключением соседних - в вершинах) и не пересекаются. Программа в первой
#  строке должна принимать число N – количество вершин многоугольника, в последующих N строках
#  – координаты соответствующих вершин (вершины задаются в последовательности против часовой стрелки).
# На выход программа должна выдавать площадь фигуры. Программа должна быть выполнена в виде блок-схемы и на ЯВУ.

cat("Введите количество вершин N: ")
N <- as.integer(readline())

x <- numeric(N)
y <- numeric(N)

cat("Введите координаты вершин (x y):\n")
for(i in 1:N) {
  coords <- strsplit(readline(), " ")[[1]]
  x[i] <- as.numeric(coords[1])
  y[i] <- as.numeric(coords[2])
}

S <- 0
for(i in 1:N) {
  j <- ifelse(i == N, 1, i + 1)
  S <- S + (x[i] * y[j] - x[j] * y[i])
}
S <- abs(S) / 2

cat(sprintf("\nПлощадь многоугольника: %.2f\n", S))
